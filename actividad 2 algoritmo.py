#actividad 2 el algoritmo de allar el perimetro y el area de un rectangulo
base=int(input(("valor de la base: ")))
#variable de la base
altura=int(input(("valor de la altura: ")))
#variable de la altura
perimetro=2*(base+altura)
#variable del perimetro
area=base*altura
#variable del area
print(f"el perimetro del resctangulo es: ",perimetro, "el area del resctangulo es: ",area)