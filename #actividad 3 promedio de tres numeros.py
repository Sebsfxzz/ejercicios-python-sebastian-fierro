#actividad 3 promedio de tres numeros
primer_numero=int(input(("escribe el primer numero: ")))
segundo_numero=int(input(("escribe el segundo numero: ")))
tercer_numero=int(input(("escribe el tercer numero: ")))
promedio=(primer_numero+segundo_numero+tercer_numero)/3
print(f"el promedio de los numeros ingresados es: ",promedio)